public class StringExercise {
    public static void main(String[] args) {
        String myStrPink = "We don't need no education";
        System.out.println(myStrPink);

        String myStringFloyd = "We don't need no thought control";
        System.out.println(myStringFloyd);

        String myStrTeen = "Check \"this\" out!, s inside of "+'s'+"!";
        System.out.println(myStrTeen);

        String myStrGates = "In Windows, the main drive is usually C:\\";
        System.out.println(myStrGates);

        String myStrJester = "I can do backslashes \\, double backslashes \\\\, " +
                "and the amazing triple backslash \\\\\\";
        System.out.println(myStrJester);
    }
}
